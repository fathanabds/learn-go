// This Go program demonstrates the use of boolean data types.
// It prints the boolean values `true` and `false` to the console.
package main

import "fmt" // Import the fmt package for formatted I/O

func main() {
	// Print the boolean value `true` with a label
	fmt.Println("benar = ", true)
	// Print the boolean value `false` with a label
	fmt.Println("salah = ", false)
}
