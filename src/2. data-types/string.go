// Package main is the entry point for the Go program.
package main

// Importing the fmt package for formatted I/O operations.
import "fmt"

// The main function is the entry point of the program.
func main() {
	// Print the string "fathan" to the console.
	fmt.Println("fathan")
	// Print the string "fathan abdul" to the console.
	fmt.Println("fathan abdul")

	// Print the length of the string "fathan" to the console.
	fmt.Println(len("fathan"))
	// Print the ASCII value of the first character of the string "fathan abdul" to the console.
	fmt.Println("fathan abdul"[0])
}
